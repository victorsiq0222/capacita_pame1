//Em todas as linguagens de programação existem diferentes tipos de dados. Em javascript , os principais tipo são:

// String (sequência de caracteres)
var nome = "Victor";
console.log(nome); // "Victor"

// Number (número)
// em python temos o int e o float
var idade = 21;
console.log(idade); // 21

// Tipo booleano = verdadeiro (true) ou falso (false)
var fluxo = true;
console.log(fluxo); // true

// Tipo null = sem valor atribuído
var nada = null;
console.log(nada); // null
